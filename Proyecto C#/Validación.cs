using System;

public class Class1
{
	public Class1()
	{
		/*
		 *		Validación
		 *	Es el proceso de verificar si un dato cumple ciertas condiciones antes de usarlo.
		 *	- evita que ocurra error.
		 *	
		 *		Validación:
		 *	verficar -> decidir -> ejecutar.
		 *		Excepción:
		 *	Ejecutar -> falla ->  manejar error.
		 *	
		 *	+++++++++++++++++++++++++++++++++++++++++++++++++++++
		 *	
		 *	Si puede fallar varias veces.
		 *		->  Usar  Validacion.
		 *			- No rompe bucle
		 *			- No consume memoria
		 *	Si no deberia Fallar, pero esta la posibilidad.
		 *		->  Usar Excepción
		 *			- Costo de Memoria Alto
		 *			- Costo alto en Runtime
	}
}
